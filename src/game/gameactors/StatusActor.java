package game.gameactors;


/**
 * An enumeration representing different statuses that a game actor can have.
 *
 * @author Tanul , Satoshi , Aditti
 * @version 1.0.0
 */
public enum StatusActor {

    /**
     * Indicates the status of the actor.
     */
    IS_PLAYER, IS_ENEMY, CAN_REST, FOLLOWING, CAN_RESPAWN, CAN_DESPAWN, CANNOT_BE_ATTACKED, IS_TRADER, IS_ALLY, HOSTILE_TO_ENEMY, IS_INVADER, HOSTILE_TO_PLAYER, IS_DEATH_RUNE_DROPPER, IS_FOLLOWER, IS_NPC, IS_POB
}
