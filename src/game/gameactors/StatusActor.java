package game.gameactors;


/**
 * An enumeration representing different statuses that a game actor can have.
 *
 * @author Tanul , Satoshi , Aditti
 * @version 1.0.0
 */
public enum StatusActor {

    /**
     * Indicates the status of the actor.
     */
    IS_PLAYER, IS_ENEMY, CAN_REST, FOLLOWING_PLAYER, CAN_RESPAWN, CAN_DESPAWN, CANNOT_BE_ATTACKED, IS_TRADER, IS_ALLY,IS_INVADER
}
