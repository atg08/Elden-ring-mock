package game.items;


/**

 An enum representing different capabilities of an Item.
 CAN_CONSUME_TO_HEAL means the Item can be consumed to heal the player's hit points.
 IS_FLASK means the Item is a flask.
 @author tanul
 */
public enum ItemUsage {
    CAN_CONSUME_TO_HEAL, EXCHANGEABLE, IS_GOLDEN_RUNE
}
